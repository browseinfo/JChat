package com.app.jchat.model;

public class LanguageVo {
	long languageId;
	String code;
	String language;
	int languageDefault;
	String image;
	String created;
	String modifed;
	int enebled;
	public long getLanguageId() {
		return languageId;
	}
	public void setLanguageId(long languageId) {
		this.languageId = languageId;
	}
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public String getLanguage() {
		return language;
	}
	public void setLanguage(String language) {
		this.language = language;
	}
	public int getLanguageDefault() {
		return languageDefault;
	}
	public void setLanguageDefault(int languageDefault) {
		this.languageDefault = languageDefault;
	}
	public String getImage() {
		return image;
	}
	public void setImage(String image) {
		this.image = image;
	}
	public String getCreated() {
		return created;
	}
	public void setCreated(String created) {
		this.created = created;
	}
	public String getModifed() {
		return modifed;
	}
	public void setModifed(String modifed) {
		this.modifed = modifed;
	}
	public int getEnebled() {
		return enebled;
	}
	public void setEnebled(int enebled) {
		this.enebled = enebled;
	}
	

}
