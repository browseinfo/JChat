package com.app.jchat.model;

public class StaticTextLangVo {
	long staticTextLanguageId;
	long staticTextID;
	long languageId;
	String title;
	String body;
	String description;
	String shortDescription;
	String keyword;
	public long getStaticTextLanguageId() {
		return staticTextLanguageId;
	}
	public void setStaticTextLanguageId(long staticTextLanguageId) {
		this.staticTextLanguageId = staticTextLanguageId;
	}
	public long getStaticTextID() {
		return staticTextID;
	}
	public void setStaticTextID(long staticTextID) {
		this.staticTextID = staticTextID;
	}
	public long getLanguageId() {
		return languageId;
	}
	public void setLanguageId(long languageId) {
		this.languageId = languageId;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getBody() {
		return body;
	}
	public void setBody(String body) {
		this.body = body;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getShortDescription() {
		return shortDescription;
	}
	public void setShortDescription(String shortDescription) {
		this.shortDescription = shortDescription;
	}
	public String getKeyword() {
		return keyword;
	}
	public void setKeyword(String keyword) {
		this.keyword = keyword;
	}
	

}
