package com.app.jchat.model;

public class CategoryLangVo  {
	long categoryLangId;
	long categoryId;
	long languageid;
	String title;
	String navigationTitle;
	String body;
	String description;
	String shortDescription;
	String keyword;
	public long getCategoryLangId() {
		return categoryLangId;
	}
	public void setCategoryLangId(long categoryLangId) {
		this.categoryLangId = categoryLangId;
	}
	public long getCategoryId() {
		return categoryId;
	}
	public void setCategoryId(long categoryId) {
		this.categoryId = categoryId;
	}
	public long getLanguageid() {
		return languageid;
	}
	public void setLanguageid(long languageid) {
		this.languageid = languageid;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getNavigationTitle() {
		return navigationTitle;
	}
	public void setNavigationTitle(String navigationTitle) {
		this.navigationTitle = navigationTitle;
	}
	public String getBody() {
		return body;
	}
	public void setBody(String body) {
		this.body = body;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getShortDescription() {
		return shortDescription;
	}
	public void setShortDescription(String shortDescription) {
		this.shortDescription = shortDescription;
	}
	public String getKeyword() {
		return keyword;
	}
	public void setKeyword(String keyword) {
		this.keyword = keyword;
	}
	

}
